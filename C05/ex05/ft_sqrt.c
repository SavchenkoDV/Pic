int	ft_sqrt(int nb)
{
	int sqrt;
	int i;

	sqrt = 0;
	i = 0;
	if (nb > 2147395600)
		return (0);
	if (nb < 0)
		return (0);
	while (sqrt <= 2147395600)
	{
		sqrt = i * i;
		if (nb == sqrt)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
